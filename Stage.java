        public static int[] convert2DArrayTo1D(int[][] _AnswerArray, int WhatStageRoom) {
                int[] AnswerArray;
                int _StageRoom;
                if (WhatStageRoom == 5) {
                        AnswerArray = new int[ROOM_5];
                        AnswerArray[ROOM_5-1] = 0;
                        _StageRoom = STAGE_ROOM_5;
                } else if (WhatStageRoom == 10) {
                        AnswerArray = new int[ROOM_10];
                        AnswerArray[ROOM_10-1] = 0;
                        _StageRoom = STAGE_ROOM_10;
                } else if (WhatStageRoom == 15) {
                        AnswerArray = new int[ROOM_15];
                        AnswerArray[ROOM_15-1] = 0;
                        _StageRoom = STAGE_ROOM_15;
                } else {
                        AnswerArray = new int[ROOM_20];
                        AnswerArray[ROOM_20-1] = 0;
                        _StageRoom = STAGE_ROOM_20;
                }

                int index = 0;
                for (int i = 0; i < _StageRoom; i++)
                        for (int j = 0; j < _StageRoom; j++)
                                AnswerArray[index++] = _AnswerArray[i][j];

                return AnswerArray;
        }


